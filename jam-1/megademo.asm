math_fast_88_mul:	proc
	;Fast 8 bit unsigned multiplication routine
	;AB -> A
	;Returns only high byte
	;Completes at whooping 15/22* cycles, killing standart mult. routine,
	;but at the price of consuming 256 bytes + alingment overhead for
	;quartersquare table, not returning lower byte and destroying B (to quartersquare of b-a)
	;In macro form, uses 10 bytes and runs at 12/13** cycles, which is already
	;a bit past 286 and is close to 386!
	; * includes calling overhead
	; ** includes a nop, placed if result is required right after multiplication
	;may get a bit of precision loss lol
	;subroutine is planned for using in real-time sound synthesis, so precision
	;isnt actually required anyways
;				     Clocks
;	Operands	PCPU	PCPU	PCPU	8086	286	386	486	Size
;			Mathlib	Fastmul	Fastmul					Bytes
;					Macro
;	reg8		53	15/22*						7+a lot for subroutine itself
;	reg8				12/13**					10/11**
;	reg8					70-77	13	9-14	13-18	2	
;	reg16		63-381							7+a lot for 16*16 subroutine itself and 16*8 8*8 support subroutines
;	reg16					118-113	21	9-22	13-26	2
;	reg32							9-38	13-42	2-4
	mov 	th,	math_quartersquare_table/256
	;(A,B) = (A-B,A+B)
	; Utilizing result being written 1 cycle after an ariphmetical operation
	add	a,	b
	sub	b,	a	;still sees original B instead of the sum
;	---	--	--	
	mov	tl,	a
	mov	a,	[tx]
	mov	tl,	b
	mov	b,	[tx]
	nop
	sub	a,	b
	ret
